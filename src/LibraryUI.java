    public void fetch_categories () {
        String queryText = "";
        PreparedStatement querySt = null;
        ResultSet answers = null;

        ArrayList<String> cats = new ArrayList<String>();

        queryText = 
                    "SELECT cat " +
                    "FROM yrb_category";

        try {
            querySt = conDB.prepareStatement (queryText);	
            answers = querySt.executeQuery();

            while (answers.next()) {
                cats.add(answers.getString("cat"));
                catComboBox.addItem(answers.getString("cat"));
                System.out.println(answers.getString("cat"));
            }
            answers.close();
            querySt.close();
        }
        catch (Exception e) {
            System.out.println("fetch_categories: " + e.toString());
        }
    }
    
    public static boolean find_customer(int cid) {
        String queryText = "";
        PreparedStatement querySt = null;
        ResultSet answers = null;

        boolean inDB = false;

        queryText = 
                    "SELECT cid, name, city " +
                    "FROM yrb_customer " +
                    "WHERE cid = ?";

        try {
            querySt = conDB.prepareStatement (queryText);	
            querySt.setInt(1, cid);
            answers = querySt.executeQuery();

            if (answers.next()) {
                inDB = true;
                custID = answers.getInt("cid");
                custName = answers.getString("name");
                custCity = answers.getString("city");
            }
            else {
                inDB = false;
            }
            answers.close();
            querySt.close();
        }
        catch (Exception e) {
            System.out.println(e.toString());
        }
        return inDB;
    }

